//链式队列结构
typedef int ElemType;

typedef struct Node
{
    ElemType data;                   //数据域指针
    struct Node *next;            //指向当前结点的下一结点
} Node;

typedef struct LQueue
{
    Node *front;                   //队头
    Node *rear;                    //队尾
    ElemType length;            //队列长度
} LQueue;

